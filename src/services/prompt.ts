import type { TargetLevel } from '../types'

const LEVEL_DESC: Record<TargetLevel, string> = {
  excellent: 'excellent（13–15 分优秀档）',
  good: 'good（10–12 分良好档）',
  pass: 'pass（7–9 分及格档）',
}

export function buildTopicGenPrompt(): string {
  return `你是一位长期在江苏省命制中考英语作文题的资深教研员。请按照江苏省（南京、苏州、无锡、扬州、常州、南通等地）近年中考英语作文真题和高频考点，命制一道全新的、适合初三学生写作的英语作文题。

【江苏中考英语作文常见高频话题类型】
1. 个人成长与品格：难忘经历、梦想、坚持/努力、责任、感恩、自信
2. 校园学习：学习方法、英语学习经历、最喜欢的老师/学科、同伴互助
3. 健康与生活方式：饮食、运动、睡眠、心理健康、远离手机
4. 家庭与人际：父母沟通、好朋友、家庭活动、写信建议
5. 社会与公益：志愿活动、保护环境、低碳生活、垃圾分类、节约资源
6. 传统文化与家乡：中国传统节日、京剧/书法/茶文化、介绍家乡（扬州/苏州/南京等江苏城市）
7. 科技与网络：网络的利弊、AI 与生活、合理使用手机
8. 时事热点：后疫情学习、双减、研学旅行、新课标素养
9. 应用文：建议信、邀请信、感谢信、给笔友的回信、发言稿

【命题要求】
1. 题目必须新颖、贴近初三学生生活，不要照搬上面任何一道既有题
2. 提供清晰的写作要求：写作对象/背景、必写要点（3 条左右）、词数（80–100）
3. 要求中英文混合表达：题目要求用中文写，方便学生理解；英文题目用规范英文
4. 给出一段可操作的"写作引导"：审题要点、思路框架、推荐词汇与句型
5. 难度适中（初三平均水平能写出 9–11 分）

请严格按以下 JSON 输出，不要任何额外文字，不要 markdown 代码块包裹：

{
  "title": "中文话题名称，6–12 字",
  "englishTitle": "规范英文标题",
  "requirements": "中文写作要求，含背景、写作要点、词数。150 字左右",
  "guidance": {
    "analysis": "审题要点，60–120 字",
    "structure": "思路框架（开头/主体/结尾），可用换行",
    "vocabulary": "推荐词汇与句型，以 / 分隔"
  }
}`
}

export function buildGradePrompt(p: {
  topic: string
  requirements: string
  studentEssay: string
  targetLevel: TargetLevel
}): string {
  return `你是一位经验丰富的中国初中英语阅卷老师，专门批改初三学生的中考英语作文。请严格按照中考评分标准批改以下作文。

【作文题目】${p.topic}
【题目要求】${p.requirements}
【学生作文】
${p.studentEssay}

【学生目标档位】${LEVEL_DESC[p.targetLevel]}

【评分标准】
- 内容（5分）：是否切题、要点是否完整、内容是否充实
- 语言（5分）：语法准确性、用词丰富度、句式多样性
- 结构（3分）：篇章连贯、逻辑清晰、过渡自然
- 书写（2分）：拼写、标点、大小写规范

【批改要求】
1. 严格按照初三学生水平评分，不要给虚高分数。初三学生平均分通常在 9-11 分
2. 批注用中文，让学生能听懂
3. 改写示范要基于学生原本的思路，不要完全推翻重写
4. 至少找出 2 个亮点句给予肯定
5. 重点改进方向要具体可操作，不要说"加强语法"这种空话

请严格按以下 JSON 格式输出，不要有任何额外文字、不要用 markdown 代码块包裹：

{
  "total_score": 数字,
  "dimension_scores": {
    "content": 数字,
    "language": 数字,
    "structure": 数字,
    "writing": 数字
  },
  "sentence_feedback": [
    {
      "original": "学生原句",
      "issue": "问题描述",
      "suggestion": "建议改写",
      "type": "grammar/word/expression/none 中的一个"
    }
  ],
  "overall_comment": "整体评语，先肯定优点再指出问题，200字左右",
  "rewrite": "基于学生原作思路的高分改写版本",
  "highlights": ["亮点句1", "亮点句2"],
  "key_improvements": ["具体改进方向1", "具体改进方向2", "具体改进方向3"]
}`
}
